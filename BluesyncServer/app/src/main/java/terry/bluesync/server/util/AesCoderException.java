package terry.bluesync.server.util;

public class AesCoderException extends Exception {
    /**
     * Creates a new exception.
     */
    public AesCoderException() {
    }

    /**
     * Creates a new exception.
     */
    public AesCoderException(String message, Throwable cause) {
        super(message, cause);
    }

    /**
     * Creates a new exception.
     */
    public AesCoderException(String message) {
        super(message);
    }

    /**
     * Creates a new exception.
     */
    public AesCoderException(Throwable cause) {
        super(cause);
    }
}
